function drawChessboard(number) {
  let pattern = "";
  //use nested for loops to create the board
  for (let i = 1; i <= number; i++) {

    // add a newline to the pattern
    // pattern += '\n';
    for (let j = 1; j <= number; j++) {
      // if the increments added together are even, add a space.
      if ((i + j) % 2 === 0) {
        pattern += " " ;
        // else add a #
      } else {
        pattern += "#"; 
      }
    }   pattern += '\n';

  } console.log(pattern)
}

