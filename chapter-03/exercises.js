function isEven(number) {
// base case
// number is 0 then return true
// if number is 1 return false
if(number === 0){
  return true
} else if(number === 1){
  return false
  // recursive case 
} else if(number < 0){
  return isEven(-number)
} else {
  return isEven(number - 2)
}
}

