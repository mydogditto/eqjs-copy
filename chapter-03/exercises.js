function countChars(string, char) {
// set up a couting variable 
let count = 0
// loop through string
for(let i = 0; i < string.length; i++){
  // if the current index is = to char
  if(string[i] === char){
    // add 1 to the count 
    count += 1
  }
 
}
 // return the count 
 return count 

}

