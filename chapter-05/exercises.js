function flatten(matrix) {
  return matrix.reduce((acc, curr) => acc.concat(curr), []);
  
}

// /////////////////////////////////////////////////////////////////////////////
// loop ////////////////////////////////////////////////////////////////////////
// /////////////////////////////////////////////////////////////////////////////

let loop = function(value, test, update, body){
  for(let i = value; test(i); i = update(i)){
    body(i);
    
  }
}

